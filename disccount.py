# Discount Calculator

# Input cost price
cost_price = float(input("Enter cost price: "))

# Calculate discount (20%)
discount = cost_price * 0.20

# Calculate total price
total_price = cost_price - discount

# Print results
print("Cost Price: $", cost_price)
print("Discount (20%): $", discount)
print("Total Price: $", total_price)
